package com.dmc.bean;

public class OrderItem {
	private String meterial = "", meterial_id = "", number = "", count_type = "", unit_price = "", total_price = "",
			description = "", size = "", order_id = "", pattern_res = "";

	public String getMeterial() {
		return meterial;
	}

	public void setMeterial(String meterial) {
		this.meterial = meterial;
	}

	public String getMeterial_id() {
		return meterial_id;
	}

	public void setMeterial_id(String meterial_id) {
		this.meterial_id = meterial_id;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getCount_type() {
		return count_type;
	}

	public void setCount_type(String count_type) {
		this.count_type = count_type;
	}

	public String getUnit_price() {
		return unit_price;
	}

	public void setUnit_price(String unit_price) {
		this.unit_price = unit_price;
	}

	public String getTotal_price() {
		return total_price;
	}

	public void setTotal_price(String total_price) {
		this.total_price = total_price;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getPattern_res() {
		return pattern_res;
	}

	public void setPattern_res(String pattern_res) {
		this.pattern_res = pattern_res;
	}

	
}
